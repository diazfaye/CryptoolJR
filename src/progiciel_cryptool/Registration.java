    private boolean CheckEmptyFields() {
        return (txtNom.getText().equals("") || txtUsername.getText().equals("")
                || txtPassword.getText().equals("") || txtCPassword.getText().equals(""));
    }

    private boolean MatchingPasswords(String p1, String p2) {
        return (p1.equals(p2));
    }

    public boolean CheckUser() {
        PreparedStatement ps;
        ResultSet rs;
        boolean userExist = false;
        String sql = "SELECT name,username  FROM utilisateur_db.tblogin WHERE name = ? AND username = ?";
        try {
            ps = ConnectionDB.getConnection().prepareStatement(sql);
            ps.setString(1, txtNom.getText());
            ps.setString(2, txtUsername.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                userExist = true;
                JOptionPane.showMessageDialog(null, "Le nom d'utilisateur a été  déjà choisis par un autre", "Error!",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Pas de connexion a la base de données", "Error!",
                    JOptionPane.ERROR_MESSAGE);
        }
        return userExist;
    }

