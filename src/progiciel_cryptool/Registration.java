    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (CheckEmptyFields()) {
            JOptionPane.showMessageDialog(null, "Please assurez vous que vous avez entré toutes vos informations", "Données manquantes",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            String name = txtNom.getText();
            String username = txtUsername.getText();
            String password = String.valueOf(txtPassword.getPassword());
            String confirmPassword = String.valueOf(txtCPassword.getPassword());
            if (!MatchingPasswords(password, confirmPassword)) {
                JOptionPane.showMessageDialog(null, "Sorry les mots de passe ne correspondent pas", "Passwords!",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                PreparedStatement pst;
                String sql = "insert into utilisateur_db.tblogin(name, username, password, confirmPassword)values(?,?,?,?)";
                try {
                    pst = ConnectionDB.getConnection().prepareStatement(sql);
                    pst.setString(1, name);
                    pst.setString(2, username);
                    pst.setString(3, password);
                    pst.setString(4, confirmPassword);
                    if (!CheckUser()) {
                        if (pst.executeUpdate() != 0) {
                            JOptionPane.showMessageDialog(null, "Votre compte a été créer avec succé", "Succé",
                                    JOptionPane.PLAIN_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(null, "Echec lors de la création de votre compte", "Echec!",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    }

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Pas de connexion a la base de données", "Error!",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

