    private void varChoixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_varChoixActionPerformed
        choix = varChoix.getSelectedItem().toString();
        if (choix.equalsIgnoreCase("cipherKey")) {
            btnFile.setText("Clésécréte");
            btnKey.setText("Clépublic");
        } else if (choix.equalsIgnoreCase("cipherMessage")) {
            btnFile.setText("Message");
            btnKey.setText("cipherKey");
        } else if (choix.equalsIgnoreCase("decipherKey")) {
            btnFile.setText("cipherKey");
            btnKey.setText("privKey");

        } else if (choix.equalsIgnoreCase("decipherMessage")) {
            btnFile.setText("ciphermessage");
            btnKey.setText("privKeydeci");

        }
    }//GEN-LAST:event_varChoixActionPerformed

