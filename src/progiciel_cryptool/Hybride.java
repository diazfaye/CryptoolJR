    private void btnDecipherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecipherActionPerformed

        try {
            FonctionsRSA fRSA = new FonctionsRSA();
            PrivateKey priv = fRSA.getPriv(txtCle, "RSA");
            fRSA.dechiffrer(priv, txtFichier, txtDestination, "RSA");
            JOptionPane.showMessageDialog(null, "Le chemin du chiffré :" + txtDestination);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur de déchiffrement:");
        }
    }//GEN-LAST:event_btnDecipherActionPerformed

    private void btnCipherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCipherActionPerformed

        try {

            FonctionsRSA fRSA = new FonctionsRSA();
            PublicKey secret = fRSA.getPub(txtCle, "RSA");

            fRSA.chiffrer(secret, txtFichier, txtDestination, "RSA");
            JOptionPane.showMessageDialog(null, "Le haché du chiffré :" + txtDestination.getBytes().hashCode());

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur de chiffrement:");
        }
    }//GEN-LAST:event_btnCipherActionPerformed

